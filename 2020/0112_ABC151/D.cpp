using info = tuple<int, int, int>;

int main()
{
  int H, W;
  cin >> H >> W;
  vector<string> S(H);
  for (auto i = 0; i < H; i++)
  {
    cin >> S[i];
  }
  int ans{0};
  for (auto i = 0; i < H; i++)
  {
    for (auto j = 0; j < W; j++)
    {
      vector<vector<int>> visited(H, vector<int>(W, -1));
      queue<info> Q;
      Q.push(info(i, j, 0));
      while (!S.empty())
      {
        int x, y, c;
        tie(x, y, c) = Q.front();
        Q.pop();
        if (visited[x][y] == -1)
        {
          visited[x][y] = c;
          for (auto k = 0; k < 4; k++)
          {
            int nx{x + dx[k]};
            int ny{y + dy[k]};
            if (0 <= nx && nx < H && 0 <= ny && ny < W && S[nx][ny] == '.' && visited[nx][ny] == -1)
            {
              Q.push(info(nx, ny, c + 1));
            }
          }
        }
      }
      for (auto x = 0; x < H; x++)
      {
        for (auto y = 0; y < W; y++)
        {
          ch_max(ans, visited[x][y]);
        }
      }
    }
  }
  cout << ans << endl;
}
