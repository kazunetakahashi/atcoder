int N;
string S;

int dp[5010][5010];

int calc(int i, int j)
{
  if (dp[i][j] < 0)
  {
    return dp[i][j];
  }
  if (!(0 <= i && i < N && 0 <= j && j < N))
  {
    dp[i][j] = 0;
  }
  else if (S[i] != S[j])
  {
    dp[i][j] = 0;
  }
  else
  {
    ch_max(dp[i][j], dp[i + 1][j + 1] + 1);
  }
  return dp[i][j];
}

int main()
{
  cin >> N >> S;
  fill(&dp[0][0], &dp[0][0] + 5010 * 5010, -1);
  int ans{0};
  for (auto i = 0; i < N; i++)
  {
    for (auto j = 0; j < N; j++)
    {
      if (i == j)
      {
        continue;
      }
      ch_max(ans, min(calc(i, j), abs(i - j)));
    }
  }
  cout << ans << endl;
}